function makeItem(input) {
  const cScore = complexityScore[input.complexity];
  const score =
    input.impact * 0.3 +
    input.parityImportance * 0.3 +
    input.leverage * 0.2 +
    (6 - cScore) * 0.1 +
    (6 - input.risk) * 0.1;
  return {
    status: input.status ?? 'Partial',
    dependencies: input.dependencies ?? [],
    labels: [...new Set([...(themeMeta[input.theme]?.labels ?? ['parity']), ...(input.labels ?? [])])],
    complexityScore: cScore,
    score: Number(score.toFixed(2)),
    ...input
  };
}

