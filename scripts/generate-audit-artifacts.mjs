async function main() {
  await fs.mkdir(auditDir, { recursive: true });
  await fs.mkdir(issueDir, { recursive: true });

  await fs.writeFile(path.join(auditDir, 'Parity_Matrix_FileMaker_vs_FMWebIDE.csv'), toCsv(), 'utf8');
  await fs.writeFile(path.join(auditDir, 'Backlog_Parity_Improvements.md'), backlogMarkdown(), 'utf8');
  await fs.writeFile(path.join(auditDir, 'Top_20_Parity_Wins_This_Week.md'), top20Markdown(), 'utf8');

  const top50 = sorted.slice(0, 50);
  for (let i = 0; i < top50.length; i += 1) {
    const item = top50[i];
    const slug = item.title.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '').slice(0, 80);
    const filename = `${String(i + 1).padStart(2, '0')}-${item.id.toLowerCase()}-${slug}.md`;
    await fs.writeFile(path.join(issueDir, filename), issueMarkdown(item, i + 1), 'utf8');
  }

  const manifest = {
    generatedAt: new Date().toISOString(),
    itemCount: items.length,
    top20: now.map((item) => item.id),
    top50IssueCount: top50.length,
    scoring: {
      impactWeight: 0.3,
      parityWeight: 0.3,
      leverageWeight: 0.2,
      complexityWeight: 0.1,
      riskWeight: 0.1
    }
  };
  await fs.writeFile(path.join(auditDir, 'audit-manifest.json'), JSON.stringify(manifest, null, 2), 'utf8');
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
